package app;


public class LGAST1 {
   private String name;
    private int count;
   private double proportion;

  
   public LGAST1(String name, int count, double proportion) {
      this.name = name;
      this.count = count;
      this.proportion = proportion;
   }

   public String getName() {
      return name;
   }
   public int getCount() {
    return count;
 }
 public double getProportion() {
    return proportion;
 }
}